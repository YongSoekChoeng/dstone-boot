DROP TABLE SAMPLE_MEMBER;
DROP TABLE SAMPLE_GROUP;
DROP TABLE SAMPLE_ROLE;
DROP TABLE SAMPLE_GROUP_ROLE;
DROP TABLE SAMPLE_ROLE_PROG;

CREATE TABLE SAMPLE_MEMBER (
  GROUP_ID VARCHAR2(30) NOT NULL, 
  USER_ID VARCHAR2(30) NOT NULL, 
  USER_PW VARCHAR2(30) ,   
  MEMBER_NAME VARCHAR2(40) NOT NULL,   
  AGE VARCHAR2(12) ,   
  DUTY VARCHAR2(40) ,     
  REGION VARCHAR2(100) , 
  ADDRESS VARCHAR2(255) , 
  ADDRESS_DTL VARCHAR2(255) ,  
  JUMINNO VARCHAR2(13) ,    
  GENDER VARCHAR2(2) ,    
  TEL VARCHAR2(15) ,    
  HP VARCHAR2(15) ,    
  EMAIL VARCHAR2(150) ,   
  INPUT_DT DATE NOT NULL,    
  UPDATE_DT DATE NOT NULL,
  PRIMARY KEY  (USER_ID)
);

CREATE TABLE SAMPLE_GROUP (
  GROUP_ID VARCHAR2(30) NOT NULL, 
  GROUP_NAME VARCHAR2(40) NOT NULL,    
  INPUT_DT DATE NOT NULL,  
  PRIMARY KEY  (GROUP_ID)
);

CREATE TABLE SAMPLE_ROLE (
  ROLE_ID VARCHAR2(30) NOT NULL, 
  ROLE_NAME VARCHAR2(40) NOT NULL,    
  INPUT_DT DATE NOT NULL,  
  PRIMARY KEY  (ROLE_ID)
);

CREATE TABLE SAMPLE_GROUP_ROLE (
  GROUP_ID VARCHAR2(30) NOT NULL, 
  ROLE_ID VARCHAR2(30) NOT NULL, 
  INPUT_DT DATE NOT NULL,  
  PRIMARY KEY  (GROUP_ID, ROLE_ID)
);

CREATE TABLE SAMPLE_ROLE_PROG (
  ROLE_ID VARCHAR2(30) NOT NULL, 
  PROG_ID VARCHAR2(30) NOT NULL, 
  PROG_NAME VARCHAR2(200), 
  PROG_URL VARCHAR2(200), 
  INPUT_DT DATE NOT NULL,  
  PRIMARY KEY  (ROLE_ID, PROG_ID)
);